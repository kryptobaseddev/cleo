// ---------------------------------------------------------------------------
// Suffix patterns stripped for alias resolution (applied in order)
// ---------------------------------------------------------------------------

/** Patterns stripped from a model ID when performing alias-fallback lookup. */
const ALIAS_STRIP_PATTERNS: ReadonlyArray<RegExp> = Object.freeze([
  /-\d{8}$/, // trailing YYYYMMDD date stamp
  /-latest$/, // trailing -latest
  /-\d+\.\d+$/, // trailing -X.Y version
]);

// ---------------------------------------------------------------------------
// Public helpers
// ---------------------------------------------------------------------------

/**
 * Look up the pricing entry for a model.
 *
 * Resolution order:
 *   1. Exact match in {@link PRICING_SNAPSHOT}.
 *   2. Alias-strip: each {@link ALIAS_STRIP_PATTERNS} is tried in order.
 *   3. `null` when no entry is found.
 *
 * @param model - Provider model ID (e.g. `'claude-sonnet-4-6-20251001'`).
 * @returns The matching {@link PricingEntry} or `null`.
 */
export function lookupPricing(model: string): PricingEntry | null {
  const exact = PRICING_SNAPSHOT[model];
  if (exact !== undefined) return exact;

  for (const pattern of ALIAS_STRIP_PATTERNS) {
    const stripped = model.replace(pattern, '');
    if (stripped !== model) {
      const alias = PRICING_SNAPSHOT[stripped];
      if (alias !== undefined) return alias;
    }
  }

  return null;
}

/**
 * Compute USD cost for a usage record at a given model's pricing tier.
 *
 * Cost breakdown:
 * - **Input tokens** → `inputCostPerMillion`
 * - **Output tokens** → `outputCostPerMillion`
 * - **Cache-read tokens** → `cacheReadCostPerMillion` (falls back to `inputCostPerMillion`)
 * - **Cache-write tokens** → `cacheWriteCostPerMillion` (falls back to `inputCostPerMillion`)
 * - **Reasoning tokens** → `outputCostPerMillion` (billed at output rate, per Anthropic/OpenAI)
 *
 * Returns `0` for unknown models — callers should log or track these separately.
 *
 * @param usage - Usage record ({@link CanonicalUsage} or {@link NormalizedUsage}).
 * @param model - Provider model ID.
 * @returns Total cost in USD.
 */
export function computeCost(usage: CanonicalUsage | NormalizedUsage, model: string): number {
  const pricing = lookupPricing(model);
  if (pricing === null) return 0;

  const canon = toCanonicalUsage(usage);

  const inputCost = (canon.inputTokens / 1_000_000) * pricing.inputCostPerMillion;
  const outputCost = (canon.outputTokens / 1_000_000) * pricing.outputCostPerMillion;
  const cacheReadCost =
    ((canon.cacheReadTokens ?? 0) / 1_000_000) *
    (pricing.cacheReadCostPerMillion ?? pricing.inputCostPerMillion);
  const cacheWriteCost =
    ((canon.cacheWriteTokens ?? 0) / 1_000_000) *
    (pricing.cacheWriteCostPerMillion ?? pricing.inputCostPerMillion);
  const reasoningCost = ((canon.reasoningTokens ?? 0) / 1_000_000) * pricing.outputCostPerMillion;

  return inputCost + outputCost + cacheReadCost + cacheWriteCost + reasoningCost;
}

/**
 * Convert a {@link NormalizedUsage} or {@link CanonicalUsage} to a full
 * {@link CanonicalUsage} record.
 *
 * Field mapping:
 * - `NormalizedUsage.cachedTokens` → `cacheReadTokens`
 * - All other optional fields default to `undefined`.
 *
 * @param u - Source usage record.
 * @returns A {@link CanonicalUsage} with all optional fields filled or `undefined`.
 */
export function toCanonicalUsage(u: NormalizedUsage | CanonicalUsage): CanonicalUsage {
  // Determine cacheReadTokens: prefer explicit cacheReadTokens, then NormalizedUsage.cachedTokens
  const cacheReadTokens: number | undefined =
    'cacheReadTokens' in u && u.cacheReadTokens !== undefined
      ? u.cacheReadTokens
      : 'cachedTokens' in u && u.cachedTokens !== undefined
        ? u.cachedTokens
        : undefined;

  return {
    inputTokens: u.inputTokens,
    outputTokens: u.outputTokens,
    cacheReadTokens,
    cacheWriteTokens: 'cacheWriteTokens' in u ? u.cacheWriteTokens : undefined,
    reasoningTokens: 'reasoningTokens' in u ? u.reasoningTokens : undefined,
    requestCount: 'requestCount' in u ? u.requestCount : undefined,
  };
}
