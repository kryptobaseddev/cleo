#!/usr/bin/env node

/**
 * inject-consolidation-checks.mjs — raw-DDL CHECK-constraint emitter for the
 * v3 consolidation migrations (SG-DB-SUBSTRATE-V2 · saga T11242 · epic T11245/E2 · T11363).
 *
 * ## Why this script exists
 *
 * `drizzle-kit` rc.3 does NOT surface a typed per-column CHECK DSL, so a
 * `drizzle-kit generate` over the consolidated `cleo-project` / `cleo-global`
 * schema families emits `CREATE TABLE`s with **no** CHECK constraints (verified:
 * `grep -c CHECK` on the freshly-generated migration returns 0). The canonical
 * mapping SSoT (`docs/migration/sqlite-schema-canonical.md` §3 / §4 / §5)
 * mandates that the boolean / timestamp / enum CHECKs ship as **raw DDL in the
 * generated migration**, and — critically — that the values be **derived from
 * the schema's own enum/boolean column metadata, NEVER hand-typed**.
 *
 * This script reads the built consolidated schema modules
 * (`packages/core/dist/store/schema/cleo-{project,global}/index.js`), introspects
 * every `sqliteTable` via drizzle's `getTableConfig`, and rewrites the matching
 * `migration.sql` in place — injecting, for each `CREATE TABLE`, the
 * table-level CHECK constraints derived from that table's columns:
 *
 *   - **§3 boolean** — every `{ mode: 'boolean' }` column (drizzle `SQLiteBoolean`)
 *     gets `CHECK ("col" IN (0, 1))`.
 *   - **§5 enum** — every `text({ enum: [...] })` column gets
 *     `CHECK ("col" IN ('a', 'b', …))`, the value list taken verbatim from the
 *     column's `enumValues` (the const array the schema declared) — never typed
 *     by hand here.
 *   - **§4 timestamp** — every TEXT column whose name ends `_at` (the canonical
 *     ISO-8601 instant convention) gets the structural GLOB guard
 *     `CHECK ("col" IS NULL OR "col" GLOB '[0-9][0-9][0-9][0-9]-[0-9][0-9]-[0-9][0-9]*')`.
 *
 * Table-level CHECKs are used (not per-column) because SQLite cannot `ALTER TABLE
 * … ADD CHECK`; the constraint must live inside the `CREATE TABLE`. The injection
 * is **idempotent** — a migration.sql that already carries an injected
 * `-- consolidation CHECK constraints (T11363)` block is skipped.
 *
 * ## Usage
 *
 *   node scripts/inject-consolidation-checks.mjs            # inject (default)
 *   node scripts/inject-consolidation-checks.mjs --check    # verify-only, exit 1 on drift
 *
 * `--check` re-derives the expected CHECK block from the live schema and asserts
 * the committed migration.sql already carries the byte-identical block — the CI
 * guard against the migration drifting from the schema's enum/boolean SSoT.
 *
 * @task T11363
 * @epic T11245
 * @saga T11242
 * @see docs/migration/sqlite-schema-canonical.md §3 (boolean) · §4 (timestamp) · §5 (enum)
 */

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { getTableConfig } from 'drizzle-orm/sqlite-core';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');

/** Sentinel comment that marks the start of the injected CHECK block. */
const BLOCK_HEADER =
  '-- consolidation CHECK constraints (T11363) — derived from schema enum/boolean/timestamp metadata, never hand-typed';

/**
 * @typedef {object} ScopePlan
 * @property {string} schemaModule - Built barrel whose `sqliteTable`s drive the CHECKs.
 * @property {string} migrationsDir - The scope's drizzle `out` directory.
 */

/** The two consolidated scopes generated by T11363. */
const SCOPES = [
  {
    schemaModule: join(REPO_ROOT, 'packages/core/dist/store/schema/cleo-project/index.js'),
    migrationsDir: join(REPO_ROOT, 'packages/core/migrations/drizzle-cleo-project'),
  },
  {
    schemaModule: join(REPO_ROOT, 'packages/core/dist/store/schema/cleo-global/index.js'),
    migrationsDir: join(REPO_ROOT, 'packages/core/migrations/drizzle-cleo-global'),
  },
];

/**
 * Escape a single-quoted SQL string literal (double any embedded quote).
 * @param {string} v
 * @returns {string}
 */
function sqlLiteral(v) {
  return `'${v.replace(/'/g, "''")}'`;
}

/**
 * Derive the ordered CHECK-constraint SQL fragments for one drizzle table,
 * straight from its column metadata. Boolean → `IN (0, 1)`; enum → `IN (...)`
 * from `enumValues`; `_at` TEXT → ISO-8601 GLOB guard.
 *
 * @param {import('drizzle-orm/sqlite-core').SQLiteTable} table
 * @returns {{ name: string, checks: string[] }}
 */
function deriveChecks(table) {
  const cfg = getTableConfig(table);
  /** @type {string[]} */
  const checks = [];
  for (const col of cfg.columns) {
    const name = col.name;
    if (col.columnType === 'SQLiteBoolean') {
      checks.push(`CHECK ("${name}" IN (0, 1))`);
      continue;
    }
    if (Array.isArray(col.enumValues) && col.enumValues.length > 0) {
      const list = col.enumValues.map(sqlLiteral).join(', ');
      checks.push(`CHECK ("${name}" IN (${list}))`);
      continue;
    }
    if (col.columnType === 'SQLiteText' && /_at$/.test(name)) {
      checks.push(
        `CHECK ("${name}" IS NULL OR "${name}" GLOB '[0-9][0-9][0-9][0-9]-[0-9][0-9]-[0-9][0-9]*')`,
      );
    }
  }
  return { name: cfg.name, checks };
}

/**
 * Build the per-table CHECK map for a schema barrel: physical table name →
 * ordered CHECK fragments. SQLite cannot `ALTER TABLE … ADD CHECK`, so each
 * table's CHECKs are later folded INTO its `CREATE TABLE` (see
 * {@link injectChecks}) as table-level constraints rather than emitted as
 * standalone statements.
 *
 * @param {Record<string, unknown>} mod - The imported schema barrel.
 * @returns {Map<string, string[]>} physical table name → ordered CHECK fragments
 */
function buildCheckMap(mod) {
  /** @type {Map<string, string[]>} */
  const map = new Map();
  for (const value of Object.values(mod)) {
    let cfg;
    try {
      cfg = getTableConfig(value);
    } catch {
      continue; // not a table export
    }
    if (!cfg || typeof cfg.name !== 'string') {
      continue;
    }
    const { name, checks } = deriveChecks(value);
    if (checks.length > 0) {
      map.set(name, checks);
    }
  }
  return map;
}

/**
 * Inject the derived CHECK fragments into each `CREATE TABLE` of a migration.sql
 * body. Each table's CHECKs are appended as table-level constraint lines before
 * the closing `\n);`. Returns the rewritten body. Idempotent: a body that
 * already carries the {@link BLOCK_HEADER} marker is returned unchanged.
 *
 * @param {string} body
 * @param {Map<string, string[]>} checkMap
 * @returns {string}
 */
function injectChecks(body, checkMap) {
  if (body.includes(BLOCK_HEADER)) {
    return body; // already injected
  }
  // Match each `CREATE TABLE `name` (\n … \n);` block and append CHECKs.
  const createRe = /CREATE TABLE `([a-z0-9_]+)` \(\n([\s\S]*?)\n\);/g;
  return body.replace(createRe, (full, tableName, inner) => {
    const checks = checkMap.get(tableName);
    if (!checks || checks.length === 0) {
      return full;
    }
    const checkLines = checks.map((c) => `\t${c}`).join(',\n');
    return `CREATE TABLE \`${tableName}\` (\n${inner},\n\t${BLOCK_HEADER}\n${checkLines}\n);`;
  });
}

/**
 * Locate the single migration.sql inside a scope's `out` directory
 * (folder-per-migration structure: `<out>/<timestamp_name>/migration.sql`).
 *
 * @param {string} migrationsDir
 * @returns {string} absolute path to migration.sql
 */
function resolveMigrationSql(migrationsDir) {
  const folders = readdirSync(migrationsDir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort();
  if (folders.length !== 1) {
    throw new Error(
      `expected exactly one migration folder in ${migrationsDir}, found ${folders.length}: ${folders.join(', ')}`,
    );
  }
  return join(migrationsDir, folders[0], 'migration.sql');
}

const checkMode = process.argv.includes('--check');
let drift = false;

for (const scope of SCOPES) {
  const mod = await import(scope.schemaModule);
  const checkMap = buildCheckMap(mod);
  const sqlPath = resolveMigrationSql(scope.migrationsDir);
  const body = readFileSync(sqlPath, 'utf8');
  // To re-derive expected output deterministically in --check mode, strip any
  // existing injected block first, then re-inject.
  const stripped = body.includes(BLOCK_HEADER)
    ? body.replace(
        /,\n\t-- consolidation CHECK constraints \(T11363\)[\s\S]*?\n(\tCHECK [\s\S]*?)\n\);/g,
        '\n);',
      )
    : body;
  const injected = injectChecks(stripped, checkMap);
  const totalChecks = [...checkMap.values()].reduce((n, c) => n + c.length, 0);

  if (checkMode) {
    if (injected !== body) {
      drift = true;
      console.error(`DRIFT: ${sqlPath} does not match schema-derived CHECK constraints`);
    } else {
      console.log(`OK: ${sqlPath} carries ${totalChecks} schema-derived CHECK constraints`);
    }
  } else {
    if (injected !== body) {
      writeFileSync(sqlPath, injected);
      console.log(`injected ${totalChecks} CHECK constraints → ${sqlPath}`);
    } else {
      console.log(`unchanged (${totalChecks} CHECK constraints already present) → ${sqlPath}`);
    }
  }
}

if (checkMode && drift) {
  process.exitCode = 1;
}
