/**
 * Rogue `.cleo/` directory forensic scanner and quarantine tool.
 *
 * Detects `.cleo/` directories that exist inside package sub-directories
 * (e.g. `packages/core/.cleo/`) — these are artefacts of agent misrouting
 * where CLEO initialised a project context inside a sub-package instead of
 * the monorepo root.
 *
 * Each rogue directory is fingerprinted with:
 *  - SHA-256 hashes for every contained file
 *  - Row counts from `tasks.db` and `brain.db`
 *  - The `__drizzle_migrations` snapshot from any SQLite database found
 *
 * Quarantine moves (never deletes) the directory to
 * `.cleo/quarantine/<pkg-name>-<ISO-timestamp>/` under the project root.
 *
 * @task T1868
 */

import { createHash } from 'node:crypto';
import type { Dirent } from 'node:fs';
import { mkdirSync, readdirSync, readFileSync, renameSync, statSync } from 'node:fs';
import { createRequire } from 'node:module';
import { basename, join, relative } from 'node:path';

// ---------------------------------------------------------------------------
// node:sqlite bootstrap (same pattern as project-health.ts)
// ---------------------------------------------------------------------------

const _require = createRequire(import.meta.url);

type DatabaseSyncModule = {
  DatabaseSync: new (
    path: string,
    options?: { readOnly?: boolean; timeout?: number },
  ) => {
    prepare(sql: string): { all(...args: unknown[]): unknown[]; get(...args: unknown[]): unknown };
    close(): void;
  };
};

let _dbCtor: DatabaseSyncModule['DatabaseSync'] | null | undefined;

function getDbCtor(): DatabaseSyncModule['DatabaseSync'] | null {
  if (_dbCtor !== undefined) return _dbCtor;
  try {
    const mod = _require('node:sqlite') as DatabaseSyncModule;
    _dbCtor = mod.DatabaseSync;
  } catch {
    _dbCtor = null;
  }
  return _dbCtor;
}

// ---------------------------------------------------------------------------
// Public types
// ---------------------------------------------------------------------------

/** SHA-256 fingerprint of a single file inside the rogue directory. */
export interface RogueFileEntry {
  /** Path relative to the rogue `.cleo/` directory root. */
  path: string;
  /** Hex-encoded SHA-256 digest. */
  sha256: string;
  /** File size in bytes. */
  size: number;
}

/** A single row from `__drizzle_migrations` in a rogue database. */
export interface DrizzleMigrationEntry {
  /** Auto-increment primary key. */
  id: number;
  /** Migration content hash recorded by Drizzle. */
  hash: string;
  /** Human-readable migration name (may be null in older schemas). */
  name: string | null;
}

/** Row counts extracted from SQLite databases found inside the rogue directory. */
export interface RogueDbRowCounts {
  /** Number of rows in `tasks.db → tasks` (undefined if DB absent or unreadable). */
  tasks?: number;
  /** Number of rows in `brain.db → brain_observations` (undefined if DB absent or unreadable). */
  brain_observations?: number;
  /** Number of rows in `brain.db → brain_decisions` (undefined if DB absent or unreadable). */
  brain_decisions?: number;
}

/**
 * Full forensic report for a single rogue `.cleo/` directory.
 *
 * Generated by {@link scanRogueCleoDirs} and consumed by
 * {@link quarantineRogueCleoDir}.
 */
export interface RogueDirReport {
  /** Absolute path to the rogue `.cleo/` directory. */
  path: string;
  /** Package directory name (e.g. `"core"` for `packages/core/.cleo/`). */
  packageName: string;
  /** SHA-256 manifest of every file inside the rogue directory. */
  fileManifest: RogueFileEntry[];
  /** Row counts from SQLite databases found in the directory. */
  dbRowCounts: RogueDbRowCounts;
  /** Drizzle migration records found in `tasks.db` (empty if absent/unreadable). */
  drizzleMigrations: DrizzleMigrationEntry[];
  /** Total size of all files in bytes. */
  totalSize: number;
  /**
   * `true` when `project-info.json` exists inside the `.cleo/` dir, which is
   * the canonical marker of a legitimately-initialised project root.
   * `false` means this is definitely a rogue directory.
   */
  hasProjectInfoMarker: boolean;
}

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

/**
 * Recursively collect all files under `dir`, returning absolute paths.
 *
 * Skips WAL sidecar files (`*.db-shm`, `*.db-wal`) from the walk so the
 * manifest only contains stable, copyable files.
 */
function collectFiles(dir: string): string[] {
  const results: string[] = [];
  const entries = readdirSync(dir, { withFileTypes: true }) as Dirent[];
  for (const entry of entries) {
    const fullPath = join(dir, String(entry.name));
    if (entry.isDirectory()) {
      results.push(...collectFiles(fullPath));
    } else if (entry.isFile()) {
      // Include WAL sidecars in manifest (for completeness) but flag them
      results.push(fullPath);
    }
  }
  return results;
}

/** Attempt to read an integer COUNT from a SQLite table. Returns `undefined` on any error. */
function safeRowCount(
  DbCtor: DatabaseSyncModule['DatabaseSync'],
  dbPath: string,
  table: string,
): number | undefined {
  try {
    const db = new DbCtor(dbPath, { readOnly: true, timeout: 3000 });
    try {
      const row = db.prepare(`SELECT COUNT(*) AS n FROM "${table}"`).get() as
        | { n: number }
        | undefined;
      return row?.n;
    } finally {
      db.close();
    }
  } catch {
    return undefined;
  }
}

/** Read the `__drizzle_migrations` table from a SQLite database. Returns [] on error. */
function readDrizzleMigrations(
  DbCtor: DatabaseSyncModule['DatabaseSync'],
  dbPath: string,
): DrizzleMigrationEntry[] {
  try {
    const db = new DbCtor(dbPath, { readOnly: true, timeout: 3000 });
    try {
      // Check the table exists first to avoid noisy errors
      const tableCheck = db
        .prepare(
          "SELECT COUNT(*) AS n FROM sqlite_master WHERE type='table' AND name='__drizzle_migrations'",
        )
        .get() as { n: number } | undefined;
      if (!tableCheck || tableCheck.n === 0) return [];

      const rows = db.prepare('SELECT id, hash, name FROM __drizzle_migrations ORDER BY id').all();
      return (rows as Array<{ id: number; hash: string; name: string | null }>).map((r) => ({
        id: r.id,
        hash: r.hash,
        name: r.name ?? null,
      }));
    } finally {
      db.close();
    }
  } catch {
    return [];
  }
}

