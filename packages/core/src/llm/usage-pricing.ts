import type { NormalizedUsage } from '@cleocode/contracts';

// ---------------------------------------------------------------------------
// Public types
// ---------------------------------------------------------------------------

/**
 * Source attribution for a pricing entry.
 */
export type PricingSource =
  | 'official_docs_snapshot' // From provider documentation (manual snapshot)
  | 'provider_cost_api' // Fetched from provider's cost API
  | 'user_override'; // User-configured in config.json

/**
 * Per-million-token cost in USD for a model.
 *
 * Cache pricing is OPTIONAL — providers without prompt caching omit those
 * fields and `computeCost` treats them as the same as input/output rates.
 *
 * @task T9274 (T-LLM-CRED Phase 3)
 */
export interface PricingEntry {
  /** USD per million prompt tokens. */
  inputCostPerMillion: number;
  /** USD per million completion tokens. */
  outputCostPerMillion: number;
  /** USD per million cache-read tokens (Anthropic ephemeral cache hit). */
  cacheReadCostPerMillion?: number;
  /** USD per million cache-write tokens (Anthropic ephemeral cache miss / first write). */
  cacheWriteCostPerMillion?: number;
  /** Attribution for this pricing entry. */
  source: PricingSource;
  /** Snapshot date YYYY-MM-DD (present for `official_docs_snapshot` entries). */
  snapshotDate?: string;
}

/**
 * Canonical usage record — superset of {@link NormalizedUsage} with reasoning
 * tokens, explicit cache-write accounting, and an optional request count for
 * cost aggregation.
 */
export interface CanonicalUsage {
  /** Tokens consumed by the prompt (non-cached). */
  inputTokens: number;
  /** Tokens generated by the model. */
  outputTokens: number;
  /** Tokens served from prompt cache (read hit). */
  cacheReadTokens?: number;
  /** Tokens written to prompt cache (cache miss / first write). */
  cacheWriteTokens?: number;
  /** Model-internal reasoning tokens (o1 / extended-thinking). Billed at output rate. */
  reasoningTokens?: number;
  /** Number of API requests included in this usage record (default 1). */
  requestCount?: number;
}

