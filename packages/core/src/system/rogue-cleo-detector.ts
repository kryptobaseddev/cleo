/**
 * Scan for rogue `.cleo/` directories inside sub-packages of a monorepo.
 *
 * A directory is considered rogue when it:
 *  1. Exists as `.cleo/` directly inside a `packages/<name>/` directory.
 *  2. Does NOT contain `project-info.json` (the canonical legitimacy marker),
 *     OR it does contain one (recorded, but still quarantine-worthy when
 *     inside a sub-package).
 *
 * The function inspects the `packages/` directory under `projectRoot`. Any
 * `.cleo/` found at depth-1 under any package is returned as a report.
 *
 * @param projectRoot - Absolute path to the monorepo root (where the canonical
 *   `.cleo/` directory lives).
 * @returns Array of forensic reports — one per rogue directory found.
 */
export function scanRogueCleoDirs(projectRoot: string): RogueDirReport[] {
  const packagesDir = join(projectRoot, 'packages');
  let packageEntries: Dirent[];
  try {
    packageEntries = readdirSync(packagesDir, { withFileTypes: true }) as Dirent[];
  } catch {
    // Not a monorepo or no packages/ directory — no rogue dirs possible.
    return [];
  }

  const DbCtor = getDbCtor();
  const reports: RogueDirReport[] = [];

  for (const pkgEntry of packageEntries) {
    if (!pkgEntry.isDirectory()) continue;
    const pkgName = String(pkgEntry.name);
    const roguePath = join(packagesDir, pkgName, '.cleo');

    let rogueStats: ReturnType<typeof statSync> | null = null;
    try {
      rogueStats = statSync(roguePath);
    } catch {
      // No .cleo dir in this package — fine.
      continue;
    }
    if (!rogueStats.isDirectory()) continue;

    // Collect file manifest (async sha256 calls resolved synchronously via
    // readFileSync equivalent — we build the list first, hashes computed below).
    const allFiles = collectFiles(roguePath);
    const fileManifest: RogueFileEntry[] = [];
    let totalSize = 0;

    for (const filePath of allFiles) {
      let size = 0;
      try {
        size = statSync(filePath).size;
      } catch {
        /* skip */
      }
      totalSize += size;

      // Synchronous hash via readFileSync
      let hash = '';
      try {
        hash = createHash('sha256').update(readFileSync(filePath)).digest('hex');
      } catch {
        hash = '';
      }

      fileManifest.push({
        path: relative(roguePath, filePath),
        sha256: hash,
        size,
      });
    }

    // Marker file
    const hasProjectInfoMarker = allFiles.some((f) => basename(f) === 'project-info.json');

    // DB row counts
    const dbRowCounts: RogueDbRowCounts = {};
    const tasksDbPath = join(roguePath, 'tasks.db');
    const brainDbPath = join(roguePath, 'brain.db');

    if (DbCtor) {
      try {
        statSync(tasksDbPath);
        dbRowCounts.tasks = safeRowCount(DbCtor, tasksDbPath, 'tasks');
      } catch {
        /* tasks.db absent */
      }
      try {
        statSync(brainDbPath);
        dbRowCounts.brain_observations = safeRowCount(DbCtor, brainDbPath, 'brain_observations');
        dbRowCounts.brain_decisions = safeRowCount(DbCtor, brainDbPath, 'brain_decisions');
      } catch {
        /* brain.db absent */
      }
    }

    // Drizzle migrations from tasks.db (primary)
    let drizzleMigrations: DrizzleMigrationEntry[] = [];
    if (DbCtor) {
      try {
        statSync(tasksDbPath);
        drizzleMigrations = readDrizzleMigrations(DbCtor, tasksDbPath);
      } catch {
        /* tasks.db absent */
      }
    }

    reports.push({
      path: roguePath,
      packageName: pkgName,
      fileManifest,
      dbRowCounts,
      drizzleMigrations,
      totalSize,
      hasProjectInfoMarker,
    });
  }

  return reports;
}

/**
 * Quarantine a single rogue `.cleo/` directory by moving it atomically to
 * `.cleo/quarantine/<pkg-name>-<ISO-timestamp>/` under the project root.
 *
 * The move is a single `fs.renameSync` which is atomic on POSIX when source
 * and destination are on the same filesystem. This is NEVER a deletion — the
 * directory and all its contents are preserved verbatim.
 *
 * @param rogue   - Report produced by {@link scanRogueCleoDirs}.
 * @param projectRoot - Absolute path to the monorepo root.
 * @returns Object containing the absolute `quarantinePath` where the directory
 *   was moved.
 * @throws If the quarantine directory cannot be created or the rename fails.
 */
export function quarantineRogueCleoDir(
  rogue: RogueDirReport,
  projectRoot: string,
): { quarantinePath: string } {
  const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
  const quarantineBase = join(projectRoot, '.cleo', 'quarantine');
  const quarantinePath = join(quarantineBase, `${rogue.packageName}-${timestamp}`);

  mkdirSync(quarantineBase, { recursive: true });
  renameSync(rogue.path, quarantinePath);

  return { quarantinePath };
}
