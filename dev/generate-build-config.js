#!/usr/bin/env node

/**
 * Build configuration generator for CLEO.
 * 
 * Generates src/config/build-config.ts from package.json values.
 * This ensures a single source of truth (SSoT) for build-time constants.
 * 
 * Run this script BEFORE TypeScript compilation to ensure build-config.ts exists.
 * 
 * @task T5245
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Read package.json
const packageJsonPath = join(__dirname, '..', 'package.json');
const packageJson = JSON.parse(readFileSync(packageJsonPath, 'utf-8'));

// Parse repository from package.json
const repoUrl = packageJson.repository?.url || 'https://github.com/kryptobaseddev/cleo.git';
const repoMatch = repoUrl.match(/github\.com\/([^/]+)\/([^/]+?)(?:\.git)?$/);
const repoOwner = repoMatch?.[1] || 'kryptobaseddev';
const repoName = repoMatch?.[2] || 'cleo';

// Build configuration object
const buildConfig = {
  // Package metadata
  name: packageJson.name,
  version: packageJson.version,
  description: packageJson.description,
  
  // Repository configuration
  repository: {
    owner: repoOwner,
    name: repoName,
    fullName: `${repoOwner}/${repoName}`,
    url: repoUrl,
    issuesUrl: `https://github.com/${repoOwner}/${repoName}/issues`,
  },
  
  // Build metadata
  buildDate: new Date().toISOString(),
  
  // Template paths (relative to package root)
  templates: {
    issueTemplatesDir: 'templates/issue-templates',
  },
};

// Generate TypeScript content
const tsContent = `/**
 * BUILD CONFIGURATION - AUTO-GENERATED FILE
 * 
 * This file is generated by dev/generate-build-config.js
 * DO NOT EDIT MANUALLY - Changes will be overwritten on next build
 * 
 * Source of truth: package.json
 * Generated at: ${buildConfig.buildDate}
 * 
 * @task T5245
 * @auto-generated
 */

export const BUILD_CONFIG = ${JSON.stringify(buildConfig, null, 2)} as const;

// Type exports for TypeScript consumers
export type BuildConfig = typeof BUILD_CONFIG;
export type RepositoryConfig = BuildConfig['repository'];
`;

// Ensure config directory exists
const configDir = join(__dirname, '..', 'src', 'config');
mkdirSync(configDir, { recursive: true });

// Write the file
const outputPath = join(configDir, 'build-config.ts');
writeFileSync(outputPath, tsContent, 'utf-8');

console.log(`✓ Generated ${outputPath}`);
console.log(`  Repository: ${buildConfig.repository.fullName}`);
console.log(`  Version: ${buildConfig.version}`);
